/**
 * src/data/projects/ 配下の全 JSON ファイルから
 * src/data/projects.ts のインポート文を自動生成する。
 *
 * Usage: node scripts/generate-project-index.mjs
 */
import { readdirSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectsDir = join(__dirname, "../src/data/projects");
const outputPath = join(__dirname, "../src/data/projects.ts");

const files = readdirSync(projectsDir)
  .filter((f) => f.endsWith(".json"))
  .sort();

const imports = files
  .map((f, i) => `import data${i} from "./projects/${f}";`)
  .join("\n");

const spreads = files.map((_, i) => `...data${i}`).join(", ");

const content = `// AUTO-GENERATED by scripts/generate-project-index.ts — DO NOT EDIT
import { Project } from "@/types/project";

${imports}

export const projects: Project[] = [${spreads}] as Project[];
`;

writeFileSync(outputPath, content, "utf-8");
console.log(
  `✅ Generated projects.ts with ${files.length} files: ${files.join(", ")}`,
);
